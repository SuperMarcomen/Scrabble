package edu.kit.informatik.scrabble.mechanics;

/**
 * An enum that describes the four directions in which you could look
 * for neighbours.
 * @author uswry
 * @version 1.0
 */
public enum Direction {

    /**
     * The direction top
     */
    TOP,
    /**
     * The direction right
     */
    RIGHT,
    /**
     * The direction bottom
     */
    BOTTOM,
    /**
     * The direction left
     */
    LEFT;
}
